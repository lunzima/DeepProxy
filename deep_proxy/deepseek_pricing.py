from __future__ import annotations

# ── 上下文窗口常量 ────────────────────────────────────────
_V4_CONTEXT_WINDOW = 1_000_000  # 1M tokens
_V4_MAX_OUTPUT = 384_000        # 最大输出 tokens

# ── 官方定价（USD / 1M tokens） ───────────────────────────
# v4-flash：官方标价（无折扣）
# v4-pro：当前限时 75% 折扣价，优惠至 2026-05-05 BST
#   原始价格：prompt $1.74 / completion $3.48
_DEEPSEEK_PRICING: dict[str, dict[str, float]] = {
    "deepseek-v4-flash": {"prompt": 0.14, "completion": 0.28},
    "deepseek-v4-pro": {"prompt": 0.435, "completion": 0.87},
}

# ── 人民币定价（元 / 1M tokens，源自官网人民币标价） ──────
# v4-flash 缓存命中：0.02 / 缓存未命中：1.0 / 输出：2.0
# v4-pro  缓存命中：0.025 / 缓存未命中：3.0 / 输出：6.0（限时 2.5 折）


def model_pricing(model_id: str) -> dict[str, float]:
    """返回模型对应的 OpenRouter 风格定价对象（USD / 1M tokens）。

    去除 ``[1m]`` 后缀后查基础模型定价。兜底返回零价，
    避免客户端因缺少字段而报错。
    """
    base = model_id.removesuffix("[1m]")
    p = _DEEPSEEK_PRICING.get(base)
    if p is not None:
        return {"prompt": p["prompt"], "completion": p["completion"], "image": 0, "request": 0}
    return {"prompt": 0, "completion": 0, "image": 0, "request": 0}
